		MOV		DPTR,#TAB
LOOP:	CLR		A
		MOVC	A,@A+DPTR
	
		CJNE	A,#0FFH,SHOW
		AJMP	START
SHOW:	MOV		P2,A
		LCALL	DELAY
		INC		DPTR
		AJMP	LOOP
		
