D2:		MOV		R6,#20
D1:		MOV		R7,#50
D3:		DJNZ	R7,D3
		DJNZ	R6,D1
		DJNZ	R5,D2
		RET
		
TAB:	DB		0E7H,0DBH,0BDH,7EH,7EH,0BDH,0DBH,0E7H
		DB		0FFH
			
		END